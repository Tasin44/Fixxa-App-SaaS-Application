class SignupSerializer(serializers.Serializer):
    email = serializers.EmailField()
    # name = serializers.CharField(max_length=150)
    password = serializers.CharField(write_only=True, min_length=8)
    referral_code = serializers.CharField(max_length=20, required=False, allow_blank=True)
    
    def validate_email(self, value):
        value = value.lower().strip()#makes email lowercase and removes spaces.
        verified_user = User.objects.filter(email=value, verified=True).exists()#.filter() returns a QuerySet, not a True/False value.
        #.exists() is used to check if any record matches (returns True or False).

        if verified_user:
            raise serializers.ValidationError("This email is already registered.")
        return value
    
    # def validate_name(self, value):
    #     return value.strip()
    
    def create(self, validated_data):
        email = validated_data['email']
        
        # It removes old unverified accounts with the same email before creating a new one.
        User.objects.filter(email=email, verified=False).delete()
        
        # Create new user
        user = User.objects.create_user(
            username=email,
            email=email,
            # first_name=validated_data['name'],
            password=validated_data['password'],
            referral_code=validated_data.get('referral_code') or None,
            verified=False
        )
        referral_code_input = validated_data.get('referral_code')
        if referral_code_input:
            try:
                ref_code_obj = ReferralCode.objects.get(code=referral_code_input)
                ReferralUse.objects.create(referral_code=ref_code_obj, used_by=user)
                ref_code_obj.trial_days_earned += 30
                ref_code_obj.save()
                # Optionally: extend owner's trial on your User/subscription model here
            except ReferralCode.DoesNotExist:
                pass  # invalid code, just ignore silently
        # Generate and send OTP
        otp_code = ''.join(random.choices(string.digits, k=6))
        '''
        Generates a 6-digit random OTP made of numbers.
        string.digits = '0123456789'
        random.choices(..., k=6) → picks 6 random digits.
        .join(...) → combines them into a single string like '483920'.
        '''
        expires_at = timezone.now() + timedelta(minutes=10)

        #Deletes any previous unused OTPs for that email before sending a new on
        OTP.objects.filter(email=email, is_used=False).delete()
        OTP.objects.create(
            email=email,
            otp_code=otp_code,
            expires_at=expires_at
        )
        
        self.send_otp_email(email, otp_code)
        
        return user
    '''
    ❓From where this 'validated_data' comes in the line def create(self, validated_data)?
    Ans:
    When DRF serializer’s .is_valid() runs successfully, it stores cleaned input data in validated_data.
    DRF automatically passes this dictionary into the create() method when .save() is called.
    👉 In short: validated_data = all the valid fields from the user’s request (after passing validation).
    
    '''


    '''
    ❓Why @staticmethod for send_otp_email?
    Ans:
    @staticmethod: Means the method doesn't need access to self (the serializer instance),or any instance data. It's a utility function that can work independently
    Use case: The method only uses the parameters passed to it (email and otp_code)

    ❓why I can use a independant function like send_otp_email in this case

    🔴email and otp_code are generated by your system inside the create() method — not sent by the user.
    🔴Since send_otp_email() just uses those values to send an email, it doesn’t need anything from the serializer instance (self).
    🔴That’s why it can be a @staticmethod — it works independently of the serializer object.
    '''
    @staticmethod
    def send_otp_email(email, otp_code):
        subject = "Your OTP Code for Verification in Fixa"
        message = f"""Dear User,

        Thank you for registering with Fixa.

        Please use the One-Time Password (OTP) below to verify your email address:

        OTP Code: {otp_code}

        This code is valid for 10 minutes. For your security, please do not share this code with anyone.

        If you did not request this, please ignore this email.

        Best regards,  
        Fixxa Support Team
        """
        #send_mail(subject, message, settings.DEFAULT_FROM_EMAIL, [email])
        send_mail(
            subject,
            message,
            settings.DEFAULT_FROM_EMAIL,
            [email],
            fail_silently=False
        )


