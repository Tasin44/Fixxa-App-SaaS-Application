from .models import ReferralCode, ReferralUse

class GenerateReferralCodeSerializer(serializers.Serializer):
    def generate_unique_code(self):
        while True:
            code = ''.join(random.choices(string.ascii_uppercase + string.digits, k=8))
            if not ReferralCode.objects.filter(code=code).exists():
                return code

    def create(self, validated_data):
        user = validated_data['user']
        if hasattr(user, 'referral_code_obj'):
            return user.referral_code_obj  # already has one
        code = self.generate_unique_code()
        return ReferralCode.objects.create(owner=user, code=code)


class ReferralStatsSerializer(serializers.ModelSerializer):
    owner_id = serializers.UUIDField(source='owner.id')
    owner_name = serializers.SerializerMethodField()
    owner_email = serializers.EmailField(source='owner.email')
    total_signups = serializers.SerializerMethodField()
    signups = serializers.SerializerMethodField()

    class Meta:
        model = ReferralCode
        fields = ['code', 'owner_id', 'owner_name', 'owner_email',
                  'trial_days_earned', 'total_signups', 'signups', 'created_at']

    def get_owner_name(self, obj):
        return f"{obj.owner.first_name} {obj.owner.last_name}".strip() or obj.owner.username

    def get_total_signups(self, obj):
        return obj.uses.count()

    def get_signups(self, obj):
        return [
            {
                "user_id": str(use.used_by.id),
                "email": use.used_by.email,
                "signed_up_at": use.used_at.isoformat()
            }
            for use in obj.uses.select_related('used_by').all()
        ]













